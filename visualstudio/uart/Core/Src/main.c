{
  /* USER CODE BEGIN StartBtn1Task */
  /* Infinite loop */
  for(;;)
  {
	  BUTTON_Process();
	  if (BUTTON_GetAction(BUTTON_DOWN) == BUTTON_SHORT_PRESS)
	  {
		  // LED on
		  HAL_GPIO_TogglePin(LED2_GPIO_Port, LED2_Pin);
	  }
	  if (BUTTON_GetAction(BUTTON_DOWN) == BUTTON_LONG_PRESS)
	  {
		  // LED on
		  for (int i = 0; i < 10; i++)
		  {
			  HAL_GPIO_TogglePin(LED2_GPIO_Port, LED2_Pin);
			  HAL_Delay(100);
		  }
		  
	  }
	  BUTTON_ResetActions();
    osDelay(1);
  }
  /* USER CODE END StartBtn1Task */
}

